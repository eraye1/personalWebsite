          <h4 id="Parenting1" class="featurette-heading">Thoughts about parenting</h4>
          <p>Ran across this comment on the internet.  Really interesting ideas about parenting.  </p>
<div class="well">          
<p>I loved this post. Growing up, I believed that love had to be earned - trouble was, I could never do enough, earn it enough, to actually receive it. The first real unconditional love I ever experienced is from the man who is my husband. This is when my journey started. Having children made me realize how much I wasn't loved - it also made me realize what love is and what it clearly isn't. I wanted something different for my kids - even took a Christian parenting course when my oldest was just a toddler which really helped me. Unconditional love doesn't mean you give them everything they want. Unconditional love means you respect the person - no matter how small they are. It means you create boundaries, explaining to them that these boundaries are not designed to 'take away their fun' but are put in place for their protection. Unconditional love means showing your kids the value of 'things' and 'people.' It's teaching them the difference about 'needs' and 'wants.' Unconditional love is not giving in to whatever your children want, it is not buying them every trinket and tech object there is ... unconditional love should instil in our kids a sense of worth. My husband and I even included giving our kids a voice - meaning they can disagree with us or challenge us on things - but it must be done in a respectful manner and vice versa. My kids are in their teens now ... and so far, its all good. And I pray that it keeps going in this direction. We have pretty open communication. We talk about lots of things, address many teen/world issues. Another aspect of unconditional love is not saying, 'this is the rule - don't do it.' We've gone so far as to explain all the whys (pros & cons) as to not do something (i.e. drugs, sex) - we cover everything and then hope like crazy that you've given them enough information for them to exercise wisdom in their choices. (They've also been taught to consider the consequences of their actions on a macro level.) This is love to me. Love is not setting your child up for failure.</p>

<p>I did not know what unconditional love was as a child - it made me selfish in that I didn't want to give of myself or my abilities unless I could get something out of it for myself. This was my 'normal' growing up. Kids are conditioned by the way the parents do things. (Thank God, He changed that very early in my walk with Him.)</p>

<p>Children do what they see and experience. Give them love - the love the Bible describes in 1 Corinthians 13:4-6. Give them respect. Give them kindness. Give them a sense of worth. Give them encouragement. Build them up. Where they are weak, help make them strong. Give a child these things and they will love you back - and they will know how to love others. They will know gratitude. They will know respect, for themselves and others. As I've shared, all three of my kids are in their teens now - and so far, its a delight!! ... </p>

<p>I am not a perfect mom - there are times I feel I fail terribly ... but they say 'love covers over a multitude of sins' ... I hope, that through it all, I have loved them enough for them to know that they were and are deeply loved. no. matter. what.</p>

<p>Dr. Kelly - I so concur, unconditional love does not produce selfish, self-centered and entitled kids. Not loving your kids, not providing for their needs (that's needs, not wants), not respecting them (age-appropriately of course), not building them up - these are things that more likely produce the undesirable. Great post.</p>          <br>
